import sharp from "sharp";
import fs from "node:fs/promises";
import path from "node:path";

// Sources sit in the sibling repo's flat assets dir. The 10 names below match
// the keys currently referenced from news markdown frontmatter / blocks.
const SRC_DIR = "/Users/vanessadumeaux/Downloads/dumeauxlab/assets";
const OUT     = "/Users/vanessadumeaux/Downloads/dumeauxlab/astro-site/src/data/news-images.js";

// Max long-edge in px. The news column is ~700px wide, so 1400 keeps things
// crisp at 2× DPR without ballooning the inlined JS payload.
const MAX_EDGE = 1400;
const JPEG_Q   = 82;

// Files to inline. Order is the order they'll appear in the output object.
const NAMES = [
  // Posts already migrated (post-2022).
  "cibcrun1.PNG",
  "Ray_MSc.png",
  "bbd_2025.jpg",
  "bbd_2026.jpg",
  "IMG_1428.png",
  "IMG_6538.jpg",
  "IMG_6541.jpg",
  "IMG_6558.jpg",
  "cell-space.jpeg",
  "breast-cancer-canada-logo.png",
  // Pre-Western (2018–2021) blog posts ported from researchweb.
  "microbiome2brain.jpg",         // 2019 Exciting times — small thumb (118×101)
  "pcrc2019_email_banner.jpg",    // 2019 PCRC banner
  "m2b_poster.png",               // 2019 microbiome2brain study poster
  "usra_vicky.png",               // 2019 USRA showcase photo
  "bbd-modelsystems-group2020.jpg", // 2020 BBD group photo
  "IMPACTT_conf_poster2021.jpg",  // 2021 IMPACTT poster
  "ca_epi_conf_2021.jpeg",        // 2021 Canadian Epigenetics flyer
];

// Logos/diagrams want to keep transparency → PNG.
// Photos go to JPEG for size. Override here as needed.
const KEEP_PNG = new Set(["breast-cancer-canada-logo.png"]);

async function encode(srcPath, name) {
  const meta = await sharp(srcPath).metadata();
  // fit:"inside" never enlarges and preserves aspect ratio.
  const pipe = sharp(srcPath)
    .rotate()
    .resize({
      width:  MAX_EDGE,
      height: MAX_EDGE,
      fit: "inside",
      withoutEnlargement: true,
    });

  let buf, mime;
  if (KEEP_PNG.has(name)) {
    buf = await pipe.png({ compressionLevel: 9 }).toBuffer();
    mime = "image/png";
  } else {
    buf = await pipe.flatten({ background: { r: 255, g: 255, b: 255 } })
                    .jpeg({ quality: JPEG_Q, mozjpeg: true })
                    .toBuffer();
    mime = "image/jpeg";
  }
  return { buf, mime, srcW: meta.width, srcH: meta.height };
}

const entries = [];
let total = 0;
for (const name of NAMES) {
  const srcPath = path.join(SRC_DIR, name);
  try { await fs.access(srcPath); } catch { console.warn(`SKIP ${name} — no source at ${srcPath}`); continue; }
  const { buf, mime, srcW, srcH } = await encode(srcPath, name);
  const b64 = buf.toString("base64");
  entries.push(`  ${JSON.stringify(name)}: "data:${mime};base64,${b64}"`);
  total += buf.length;
  console.log(`${name.padEnd(34)} src=${srcW}x${srcH} → ≤${MAX_EDGE}px  (${(buf.length/1024).toFixed(0)} KB ${mime.split("/")[1]})`);
}

const js =
  `// Auto-generated by scripts/regen-news-images.mjs — do not edit by hand.\n` +
  `// Sources live in ../../../assets/. Long-edge cap ${MAX_EDGE}px, JPEG q${JPEG_Q}.\n` +
  `export const NEWS_IMAGES = {\n${entries.join(",\n")}\n};\n`;

await fs.writeFile(OUT, js);
console.log(`\nWrote ${OUT}`);
console.log(`  raw image bytes:  ${(total/1024/1024).toFixed(2)} MB`);
console.log(`  JS file on disk: ${(js.length/1024/1024).toFixed(2)} MB (base64 ~4/3 of raw)`);
